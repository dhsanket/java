public class Movie {
    String title;
    String year;
    Double rating;

    public String getTitle() {
        return title;
    }

    public String getYear() {
        return year;
    }

    public Double getRating() {
        return rating;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public void setYear(String year) {
        this.year = year;
    }

    public void setRating(Double rating) {
        this.rating = rating;
    }
}
